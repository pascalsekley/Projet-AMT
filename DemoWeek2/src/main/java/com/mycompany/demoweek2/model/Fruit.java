package com.mycompany.demoweek2.model;

public class Fruit {
    
    private String name;
    private String color;

    public Fruit(String name, String color) {
        this.name = name;
        this.color = color;
    }
    
    

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }
        
    
}
